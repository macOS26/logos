import Foundation

// Polygon Variants
enum PolygonVariant: String, CaseIterable {
    case pentagon = "Pentagon (5 sides)"
    case hexagon = "Hexagon (6 sides)"
    case heptagon = "Heptagon (7 sides)"
    case octagon = "Octagon (8 sides)"
    case nonagon = "Nonagon (9 sides)"
}
